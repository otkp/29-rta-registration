package org.epragati.rules.util;

public class SBIOtherTransactionsParts {

	private String otherTransactionsDescription;
	private Double amount;
	private String creditAccount;

	/**
	 * @return the otherTransactionsDescription
	 */
	public String getOtherTransactionsDescription() {
		return otherTransactionsDescription;
	}

	/**
	 * @param otherTransactionsDescription
	 *            the otherTransactionsDescription to set
	 */
	public void setOtherTransactionsDescription(String otherTransactionsDescription) {
		this.otherTransactionsDescription = otherTransactionsDescription;
	}

	/**
	 * @return the amount
	 */
	public Double getAmount() {
		return amount;
	}

	/**
	 * @param amount
	 *            the amount to set
	 */
	public void setAmount(Double amount) {
		this.amount = amount;
	}

	/**
	 * @return the creditAccount
	 */
	public String getCreditAccount() {
		return creditAccount;
	}

	/**
	 * @param creditAccount
	 *            the creditAccount to set
	 */
	public void setCreditAccount(String creditAccount) {
		this.creditAccount = creditAccount;
	}

}
